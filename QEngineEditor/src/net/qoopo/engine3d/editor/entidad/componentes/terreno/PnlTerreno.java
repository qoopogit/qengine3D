    private void aplicarCambios() {
        //elimino los componentes de geometria que se haya generado anteriormente
        QUtilComponentes.eliminarComponenteGeometria(terreno.entidad);
        if (textura != null) {
            textura.setMuestrasU(Float.valueOf(txtMU_Difusa.getText()));
            textura.setMuestrasV(Float.valueOf(txtMV_Difusa.getText()));
        }
        if (imagen != null) {
            terreno.generar(imagen, Float.valueOf(txtTamCelda.getText()), Float.valueOf(txtMinY.getText()), Float.valueOf(txtMaxY.getText()), textura, Integer.valueOf(txtOffset.getText()));
        }
    }

